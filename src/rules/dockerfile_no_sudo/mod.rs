//! dockerfile-no-sudo — sudo is unavailable and unnecessary in container
//! builds; commands run as root unless USER says otherwise.

mod typescript;

use crate::diagnostic::Severity;
use crate::files::Language;
use crate::rules::RuleDef;
use crate::rules::backend::Backend;
use crate::rules::meta::RuleMeta;

pub const META: RuleMeta = RuleMeta {
    id: "dockerfile-no-sudo",
    description: "Do not use `sudo` inside a Dockerfile RUN.",
    remediation: "Remove `sudo` and switch users via `USER` if needed.",
    severity: Severity::Warning,
    doc_url: None,
    categories: &["docker"],
};

pub fn register() -> RuleDef {
    RuleDef {
        meta: META,
        backends: vec![(
            Language::Dockerfile,
            Backend::TreeSitter(Box::new(typescript::Check)),
        )],
    }
}
