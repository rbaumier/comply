//! dockerfile-single-healthcheck — only the last HEALTHCHECK takes effect, so
//! multiple HEALTHCHECKs in the same image are misleading.

mod check;

use crate::diagnostic::Severity;
use crate::files::Language;
use crate::rules::RuleDef;
use crate::rules::backend::Backend;
use crate::rules::meta::RuleMeta;

pub const META: RuleMeta = RuleMeta {
    id: "dockerfile-single-healthcheck",
    description: "Only one HEALTHCHECK should appear per image.",
    remediation: "Remove duplicate HEALTHCHECK instructions; only the last one is effective.",
    severity: Severity::Warning,
    doc_url: None,
    categories: &["docker"],

    skip_in_test_dir: false,
    skip_in_relaxed_dir: false,
};

pub fn register() -> RuleDef {
    RuleDef {
        meta: META,
        backends: vec![(
            Language::Dockerfile,
            Backend::TreeSitter(Box::new(check::Check)),
        )],
    }
}
