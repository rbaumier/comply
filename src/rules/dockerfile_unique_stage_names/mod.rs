//! dockerfile-unique-stage-names — duplicate FROM AS aliases shadow each
//! other and confuse `COPY --from`.

mod typescript;

use crate::diagnostic::Severity;
use crate::files::Language;
use crate::rules::RuleDef;
use crate::rules::backend::Backend;
use crate::rules::meta::RuleMeta;

pub const META: RuleMeta = RuleMeta {
    id: "dockerfile-unique-stage-names",
    description: "Each FROM stage alias must be unique.",
    remediation: "Rename the duplicate `AS <alias>` to a unique value.",
    severity: Severity::Warning,
    doc_url: None,
    categories: &["docker"],
};

pub fn register() -> RuleDef {
    RuleDef {
        meta: META,
        backends: vec![(
            Language::Dockerfile,
            Backend::TreeSitter(Box::new(typescript::Check)),
        )],
    }
}
