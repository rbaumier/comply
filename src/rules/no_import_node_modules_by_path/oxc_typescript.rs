//! no-import-node-modules-by-path OXC backend — flag ES imports and `require()`
//! calls whose specifier references `node_modules/` directly.

use crate::diagnostic::{Diagnostic, Severity};
use crate::oxc_helpers::byte_offset_to_line_col;
use crate::rules::backend::{AstKind, AstType, CheckCtx, OxcCheck};
use oxc_ast::ast::{Argument, Expression};
use std::sync::Arc;

pub struct Check;

fn specifier_is_node_modules_path(spec: &str) -> bool {
    spec.contains("/node_modules/") || spec.starts_with("node_modules/")
}

impl OxcCheck for Check {
    fn interested_kinds(&self) -> &'static [AstType] {
        &[AstType::ImportDeclaration, AstType::CallExpression]
    }

    fn run<'a>(
        &self,
        node: &oxc_semantic::AstNode<'a>,
        ctx: &CheckCtx,
        _semantic: &'a oxc_semantic::Semantic<'a>,
        diagnostics: &mut Vec<Diagnostic>,
    ) {
        match node.kind() {
            AstKind::ImportDeclaration(import) => {
                let spec = import.source.value.as_str();
                if !specifier_is_node_modules_path(spec) {
                    return;
                }
                let (line, column) =
                    byte_offset_to_line_col(ctx.source, import.source.span.start as usize);
                diagnostics.push(Diagnostic {
                    path: Arc::clone(&ctx.path_arc),
                    line,
                    column,
                    rule_id: super::META.id.into(),
                    message: format!(
                        "Import specifier `{spec}` references node_modules directly — import by package name instead."
                    ),
                    severity: Severity::Warning,
                    span: None,
                });
            }
            AstKind::CallExpression(call) => {
                let Expression::Identifier(callee) = &call.callee else { return };
                if callee.name.as_str() != "require" {
                    return;
                }
                for arg in &call.arguments {
                    let Argument::StringLiteral(s) = arg else { continue };
                    let spec = s.value.as_str();
                    if specifier_is_node_modules_path(spec) {
                        let (line, column) =
                            byte_offset_to_line_col(ctx.source, s.span.start as usize);
                        diagnostics.push(Diagnostic {
                            path: Arc::clone(&ctx.path_arc),
                            line,
                            column,
                            rule_id: super::META.id.into(),
                            message: format!(
                                "`require(\"{spec}\")` references node_modules directly — require by package name instead."
                            ),
                            severity: Severity::Warning,
                            span: None,
                        });
                    }
                }
            }
            _ => {}
        }
    }
}

#[cfg(test)]
impl crate::rules::test_helpers::RunRule for Check {
    fn meta(&self) -> &'static crate::rules::meta::RuleMeta {
        &super::META
    }
    fn execute_with_ctx(
        &self,
        src: &str,
        path: &std::path::Path,
        project: &crate::project::ProjectCtx,
        file: &crate::rules::file_ctx::FileCtx,
    ) -> Vec<crate::diagnostic::Diagnostic> {
        crate::rules::test_helpers::run_oxc_check(self, src, path, project, file)
    }
}
#[cfg(test)]
mod tests {
    use super::*;

    fn run_on(s: &str) -> Vec<Diagnostic> {
        crate::rules::test_helpers::run_rule(&Check, s, "t.ts")
    }

    #[test]
    fn flags_import_with_node_modules_in_middle() {
        assert_eq!(
            run_on("import foo from \"../../node_modules/foo/dist/index.js\"").len(),
            1
        );
    }

    #[test]
    fn flags_import_starting_with_node_modules() {
        assert_eq!(run_on("import foo from 'node_modules/foo'").len(), 1);
    }

    #[test]
    fn flags_require_with_node_modules_path() {
        assert_eq!(
            run_on("const foo = require(\"../node_modules/foo/index.js\")").len(),
            1
        );
    }

    #[test]
    fn allows_package_name_import() {
        assert!(run_on("import foo from \"foo\"").is_empty());
    }

    #[test]
    fn allows_scoped_package_import() {
        assert!(run_on("import { bar } from \"@scope/pkg\"").is_empty());
    }

    #[test]
    fn allows_require_by_package_name() {
        assert!(run_on("const foo = require(\"foo\")").is_empty());
    }

    #[test]
    fn allows_relative_import_not_through_node_modules() {
        assert!(run_on("import foo from \"./utils/foo\"").is_empty());
    }
}
