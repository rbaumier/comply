//! no-import-node-modules-by-path — flag ES imports and `require()` calls whose
//! specifier references `node_modules/` directly instead of the package name.

use crate::diagnostic::{Diagnostic, Severity};

fn specifier_is_node_modules_path(spec: &str) -> bool {
    spec.contains("/node_modules/") || spec.starts_with("node_modules/")
}

fn trim_quotes(s: &str) -> &str {
    s.trim_matches(|c: char| c == '\'' || c == '"' || c == '`')
}

crate::ast_check! { |node, source, ctx, diagnostics|
    match node.kind() {
        "import_statement" => {
            let Some(source_node) = node.child_by_field_name("source") else { return };
            let spec = trim_quotes(source_node.utf8_text(source).unwrap_or(""));
            if !specifier_is_node_modules_path(spec) {
                return;
            }
            diagnostics.push(Diagnostic::at_node(
                ctx.path,
                &source_node,
                super::META.id,
                format!(
                    "Import specifier `{spec}` references node_modules directly — import by package name instead."
                ),
                Severity::Warning,
            ));
        }
        "call_expression" => {
            let Some(func) = node.child_by_field_name("function") else { return };
            if func.utf8_text(source).unwrap_or("") != "require" {
                return;
            }
            let Some(args) = node.child_by_field_name("arguments") else { return };
            let mut cursor = args.walk();
            for child in args.named_children(&mut cursor) {
                if child.kind() != "string" {
                    continue;
                }
                let spec = trim_quotes(child.utf8_text(source).unwrap_or(""));
                if specifier_is_node_modules_path(spec) {
                    diagnostics.push(Diagnostic::at_node(
                        ctx.path,
                        &child,
                        super::META.id,
                        format!(
                            "`require(\"{spec}\")` references node_modules directly — require by package name instead."
                        ),
                        Severity::Warning,
                    ));
                }
            }
        }
        _ => {}
    }
}

#[cfg(test)]
mod tests {
    use super::Check;
    use crate::diagnostic::Diagnostic;

    fn run(s: &str) -> Vec<Diagnostic> {
        crate::rules::test_helpers::run_ts(s, &Check)
    }

    #[test]
    fn flags_import_with_node_modules_in_middle() {
        assert_eq!(
            run("import foo from \"../../node_modules/foo/dist/index.js\"").len(),
            1
        );
    }

    #[test]
    fn flags_import_starting_with_node_modules() {
        assert_eq!(
            run("import foo from 'node_modules/foo'").len(),
            1
        );
    }

    #[test]
    fn flags_require_with_node_modules_path() {
        assert_eq!(
            run("const foo = require(\"../node_modules/foo/index.js\")").len(),
            1
        );
    }

    #[test]
    fn allows_package_name_import() {
        assert!(run("import foo from \"foo\"").is_empty());
    }

    #[test]
    fn allows_scoped_package_import() {
        assert!(run("import { bar } from \"@scope/pkg\"").is_empty());
    }

    #[test]
    fn allows_require_by_package_name() {
        assert!(run("const foo = require(\"foo\")").is_empty());
    }

    #[test]
    fn allows_relative_import_not_through_node_modules() {
        assert!(run("import foo from \"./utils/foo\"").is_empty());
    }
}
